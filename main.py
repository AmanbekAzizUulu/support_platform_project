def introduce_error(text):
    # —Å–ª—É—á–∞–π–Ω—ã–π –ø—Ä–æ–ø—É—Å–∫ –±—É–∫–≤—ã
    if random.random() < 0.1:
        idx = random.randint(0, len(text)-1)
        text = text[:idx] + text[idx+1:]
    # —Å–ª—É—á–∞–π–Ω—ã–π —Å–∏–º–≤–æ–ª –≤ –≤–µ—Ä—Ö–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ
    elif random.random() < 0.1:
        idx = random.randint(0, len(text)-1)
        text = text[:idx] + text[idx].upper() + text[idx+1:]
    # —Å–ª—É—á–∞–π–Ω–∞—è –æ–ø–µ—á–∞—Ç–∫–∞
    elif random.random() < 0.1:
        typos = {
            "–∞": "–æ", "–æ": "–∞", "–µ": "–∏", "–∏": "–µ",
            "–Ω": "–º", "–≤": "—Ñ", "–ø": "—Ä", "—Ç": "–¥"
        }
        idx = random.randint(0, len(text)-1)
        if text[idx].lower() in typos:
            text = text[:idx] + typos[text[idx].lower()] + text[idx+1:]
    return text

